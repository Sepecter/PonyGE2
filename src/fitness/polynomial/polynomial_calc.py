def evaluate_polynomial(poly, values):
    expr = simplify(expand(poly))

    # 检查是否是平凡多项式（值恒等于 0）
    if expr == 0 or type(expr) == sympy.core.numbers.Float:
        return 5000000

    # 将多项式字符串转化为表达式，并简化
    Er = symbols('C')
    equation = Eq(expr, 0)
    solution = solve(equation, Er)

    cnt1 = 0
    cnt2 = 0
    res1 = 0
    res2 = 0
    has_I1 = 0
    has_I2 = 0
    for i in values:

        tmp1 = solution[0].subs(i) - i['C']
        tmp2 = solution[1].subs(i) - i['C']
        if tmp1.has(I):
            has_I1 = 1
        if tmp2.has(I):
            has_I2 = 1
        res1 += tmp1 * tmp1
        res2 += tmp2 * tmp2
        if abs(tmp1 / i['C']) or has_I1 > 0.1:
            cnt1 += 1
        if abs(tmp2 / i['C']) or has_I2 > 0.1:
            cnt2 += 1
    res1 /= 27
    res2 /= 27
    if has_I1:
        res1 = 5000000
    if has_I2:
        res2 = 5000000
    result = min(res1, res2)
    cnt = min(cnt1, cnt2)
    if cnt > 7:
        result += 5000
    result = math.sqrt(result)

    # # 用给定的变量值代入多项式表达式
    # cnt = 0
    # result = 0
    # for i in values:
    #     tmp = expr.subs(i) - i['G']
